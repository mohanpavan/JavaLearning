package collections.arraylist;

public class Address {
    public int hNo;
    public String area;
    public String landMark;
    public int pinCode;

    public Address(int hNo, String area, String landMark, int pinCode){
        this.hNo = hNo;
        this.area = area;
        this.landMark = landMark;
        this.pinCode = pinCode;
    }
}
